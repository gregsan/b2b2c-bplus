export function getInsuranceShortList(type: string): string[] {
  const insurance = insuranceByType[type] || insuranceByType.bank
  return insurance.map(item => item.title)
}

export function getInsuranceSummary(type: string): string {
  switch (type) {
    case 'bank':
      return 'Страхування подорожей, заходів і домашніх улюбленців'
    case 'operator':
      return 'Страхування телефону, кібербезпека та захист даних'
    case 'gas-station':
      return 'Допомога з доступом та евакуацією авто'
    default:
      return 'Комплексне страхування'
  }
}